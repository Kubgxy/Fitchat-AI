const storage = multer.diskStorage({
  destination: (req, file, cb) => {
    cb(null, "uploads/"); // ‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏õ‡∏•‡∏≤‡∏¢‡∏ó‡∏≤‡∏á
  },
  filename: (req, file, cb) => {
    const uniqueSuffix = Date.now() + "-" + Math.round(Math.random() * 1e9);
    const ext = path.extname(file.originalname);
    cb(null, file.fieldname + "-" + uniqueSuffix + ext);
  },
});

const upload = multer({ storage }); // ‚úÖ ‡∏ï‡∏±‡∏ß‡πÅ‡∏õ‡∏£‡∏ô‡∏µ‡πâ‡πÅ‡∏´‡∏•‡∏∞‡∏ó‡∏µ‡πà‡πÉ‡∏ä‡πâ‡∏Å‡∏±‡∏ö upload.single()

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏™‡∏£‡πâ‡∏≤‡∏á user_id ‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡∏ã‡πâ‡∏≥
const generateUniqueUserId = async (): Promise<string> => {
  let userId: string;
  let isUnique = false;

  while (!isUnique) {
    userId = Math.floor(1000 + Math.random() * 9000).toString(); // ‡∏™‡∏∏‡πà‡∏°‡πÄ‡∏•‡∏Ç 4 ‡∏´‡∏•‡∏±‡∏Å
    const existingUser = await User.findOne({ user_id: userId });
    if (!existingUser) {
      isUnique = true;
    }
  }

  return userId!;
};

